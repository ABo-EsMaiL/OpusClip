    final_path  = os.path.join(clips_dir, f"{base}_FINAL.mp4")

    if os.path.exists(final_path):
        print(f"  ✓  Clip {n} already rendered — skip")
        return final_path

    print(f"\n{'─'*62}")
    print(f"🎬  Clip {n}/{len(selected_clips)}  ★{clip['virality_score']}")
    print(f"    {title[:60]}")
    print(f"    {c_start:.1f}s → {c_end:.1f}s  ({duration:.0f}s)")
    t0 = time.time()

    # ── Step 1: Two-stage seek  (PERFORMANCE KEY FIX) ─────────────
    # Stage A: input seek to keyframe ≤10s before clip  (fast, imprecise)
    # Stage B: output seek for remaining seconds        (slow but only ≤10s)
    print("    [1/4] Cutting raw clip …")
    PRE_SEEK  = max(0.0, c_start - 10.0)
    FINE_SEEK = c_start - PRE_SEEK          # 0–10 s to decode precisely

    r = subprocess.run([
        "ffmpeg", "-y",
        "-ss", f"{PRE_SEEK:.3f}",            # A: jump to keyframe (microseconds)
        "-i",  video_path,
        "-ss", f"{FINE_SEEK:.3f}",           # B: fine-tune from there (≤10s)
        "-t",  f"{duration + 0.5:.3f}",      # +0.5s safety buffer
        "-c:v", "libx264", "-preset", "ultrafast", "-crf", "22",
        "-c:a", "aac", "-b:a", "128k",
        raw_path,
    ], capture_output=True, text=True)
    if r.returncode != 0:
        raise RuntimeError(f"Raw clip failed:\n{r.stderr[-400:]}")

    # Extract audio
    audio_filter = "loudnorm=I=-16:TP=-1.5:LRA=11" if NORMALISE_AUDIO else "anull"
    subprocess.run([
        "ffmpeg", "-y", "-i", raw_path,
        "-vn", "-af", audio_filter,
        "-c:a", "aac", "-b:a", "128k", audio_path,
    ], check=True, capture_output=True)

    # ── Step 2: Face scan at 480p — every FACE_DETECT_EVERY frames ──
    print("    [2/4] Scanning faces …")
    SCAN_H     = min(480, vid_h)
    scan_scale = SCAN_H / vid_h
    SCAN_W     = int(vid_w * scan_scale)
    if SCAN_W % 2: SCAN_W += 1

    r = subprocess.run([
        "ffmpeg", "-y", "-i", raw_path,
        "-vf", f"scale={SCAN_W}:{SCAN_H}",
        "-c:v", "libx264", "-preset", "ultrafast", "-crf", "28",
        "-an", scan_path,
    ], capture_output=True, text=True)
    if r.returncode != 0:
        raise RuntimeError(f"Scan failed:\n{r.stderr[-400:]}")

    scan_cap     = cv2.VideoCapture(scan_path)
    scan_area    = SCAN_W * SCAN_H
    frames_faces = []
    last_faces   = []
    fi = 0
    while True:
        ok, frame = scan_cap.read()
        if not ok: break
        if fi % FACE_DETECT_EVERY == 0:       # ← detect only every 3rd frame
            last_faces = detect_faces(frame, scan_area, scan_scale)
        frames_faces.append(last_faces)        # rest inherit last result
        fi += 1
    scan_cap.release()
    os.remove(scan_path)
    n_frames = len(frames_faces)
    detected = sum(1 for ff in frames_faces if ff)
    print(f"         {n_frames} frames  |  faces in {detected} frames  "
          f"(sampled every {FACE_DETECT_EVERY})")

    # ── Step 3: Director decisions per frame ──────────────────────
    director = SmartDirector(
        vid_w, vid_h, src_crop_w, vid_fps,
        speaking_mar  = CONFIG["speaking_mar"],
        min_face_area = CONFIG["min_face_area"],
        debounce_s    = CONFIG["state_debounce_s"],
    )
    director.smooth_x = vid_w // 2
    frame_data = [(director.update(ff), director.state)
                  for ff in frames_faces]

    # ── Step 4: Render with smart crop ───────────────────────────
    print("    [3/4] Rendering smart crop …")
    ffpipe = subprocess.Popen([
        "ffmpeg", "-y",
        "-f", "rawvideo", "-vcodec", "rawvideo",
        "-pix_fmt", "bgr24", "-s", f"{tgt_w}x{tgt_h}",
        "-r", str(vid_fps), "-i", "pipe:0",
        "-vcodec", "libx264", "-preset", "fast",
        "-crf", str(crf), "-pix_fmt", "yuv420p",
        silent_path,
    ], stdin=subprocess.PIPE, stderr=subprocess.DEVNULL)

    raw_cap    = cv2.VideoCapture(raw_path)
    fi         = 0
    best_frame = None      # for thumbnail
    best_face_area = 0.0

    while True:
        ok, frame = raw_cap.read()
        if not ok: break

        cx, fstate = frame_data[fi] if fi < len(frame_data) else frame_data[-1]

        if fstate == SmartDirector.BROLL:
            resized = make_broll_frame(frame, tgt_w, tgt_h)
        else:
            cx      = max(0, min(cx, vid_w - src_crop_w))
            cropped = frame[:, cx : cx + src_crop_w]
            resized = cv2.resize(cropped, (tgt_w, tgt_h),
                                 interpolation=cv2.INTER_LINEAR)
            # Track best frame for thumbnail (largest face, mid-clip range)
            if SAVE_THUMBNAIL and frames_faces[fi] and \
               n_frames * 0.25 < fi < n_frames * 0.75:
                fa = max(f["area_ratio"] for f in frames_faces[fi])
                if fa > best_face_area:
                    best_face_area = fa
                    best_frame     = resized.copy()

        ffpipe.stdin.write(resized.tobytes())
        fi += 1

    raw_cap.release()
    ffpipe.stdin.close()
    ffpipe.wait()

    # Save thumbnail
    if SAVE_THUMBNAIL and best_frame is not None:
        cv2.imwrite(thumb_path, best_frame, [cv2.IMWRITE_JPEG_QUALITY, 92])

    # ── Step 5: Subtitles + merge ─────────────────────────────────
    print("    [4/4] Burning subtitles …")
    ass_content = build_ass(words, c_start, c_end, tgt_w, tgt_h, title)
    with open(ass_path, "w", encoding="utf-8") as f:
        f.write(ass_content)
    shutil.copy(ass_path, safe_ass)

    # Build video filter: subtitles + optional fade
    vf_parts = [f"subtitles={safe_ass}"]
    if ADD_FADE:
        fade_d = 0.4
        vf_parts += [
            f"fade=t=in:st=0:d={fade_d}",
            f"fade=t=out:st={max(0, duration-fade_d):.2f}:d={fade_d}",
        ]
    vf = ",".join(vf_parts)

    merge = subprocess.run([
        "ffmpeg", "-y",
        "-i", silent_path, "-i", audio_path,
        "-vf", vf,
        "-c:v", "libx264", "-preset", "fast", "-crf", str(crf),
        "-c:a", "aac", "-b:a", "128k",
        "-shortest", final_path,
    ], capture_output=True, text=True)

    if merge.returncode != 0:
        print(f"     ⚠  Subtitle burn failed → saving without subs")
        subprocess.run([
            "ffmpeg", "-y", "-i", silent_path, "-i", audio_path,
            "-c:v", "copy", "-c:a", "copy", "-shortest", final_path,
        ], check=True, capture_output=True)

    for p in [raw_path, silent_path, audio_path, safe_ass]:
        try: os.remove(p)
        except: pass

    mb = os.path.getsize(final_path) / 1_000_000
    print(f"    ✅  {mb:.1f} MB — {time.time()-t0:.0f}s → "
          f"{os.path.basename(final_path)}"
          + (f"  + thumb" if SAVE_THUMBNAIL and best_frame is not None else ""))
    return final_path


