# Characters known to render as hollow boxes in libass / Cairo / Tajawal
_BAD_CHARS = re.compile(
    "["
    # Emoji ranges (full)
    u"\U0001F600-\U0001F64F\U0001F300-\U0001F5FF"
    u"\U0001F680-\U0001F6FF\U0001F700-\U0001F77F"
    u"\U0001F780-\U0001F7FF\U0001F800-\U0001F8FF"
    u"\U0001F900-\U0001F9FF\U0001FA00-\U0001FA6F"
    u"\U00002702-\U000027B0\U000024C2-\U0001F251"
    u"\U0001F1E0-\U0001F1FF"
    # Control / non-printable / BOM / zero-width space
    u"\U00000000-\U0000001F"   # C0 controls
    u"\U0000007F-\U0000009F"   # DEL + C1 controls
    u"\U0000200B"              # zero-width space
    u"\U0000FEFF"              # BOM / ZWNBSP
    # Variation selectors and language tags (invisible but cause boxes)
    u"\U0000FE00-\U0000FE0F"
    u"\U000E0000-\U000E007F"
    "]+",
    flags=re.UNICODE,
)

def clean_for_subtitle(text: str) -> str:
    """
    Whitelist-based cleaning — character by character.
    Drops EVERYTHING that's not explicitly Arabic or Latin.
    No regex, no missing emoji ranges, no bugs.
    """
    if not text:
        return ""
    text = unicodedata.normalize("NFC", text)
    result = []
    for ch in text:
        cp = ord(ch)
        keep = (
            0x0020 <= cp <= 0x007E or   # ASCII printable (letters, digits, punct)
            0x00C0 <= cp <= 0x024F or   # Latin Extended A/B (é, ü, etc.)
            0x0600 <= cp <= 0x06FF or   # Arabic main block
            0x0750 <= cp <= 0x077F or   # Arabic Supplement
            0x08A0 <= cp <= 0x08FF or   # Arabic Extended-A
            0xFB50 <= cp <= 0xFDFF or   # Arabic Presentation Forms-A
            0xFE70 <= cp <= 0xFEFF or   # Arabic Presentation Forms-B
            cp == 0x0640 or             # Arabic Tatweel (kashida ـ)
            cp in (
                0x060C,  # Arabic comma ،
                0x061B,  # Arabic semicolon ؛
                0x061F,  # Arabic question mark ؟
                0x200C,  # ZWNJ (needed for correct Arabic rendering)
                0x200D,  # ZWJ  (needed for correct Arabic rendering)
                0x2019,  # Right single quote '
                0x201C,  # Left double quote "
                0x201D,  # Right double quote "
                0x2026,  # Ellipsis …
                0x2013,  # En dash –
                0x2014,  # Em dash —
            )
        )
        if keep:
            result.append(ch)
    # Collapse multiple spaces into one
    return " ".join("".join(result).split())


def is_arabic_text(text: str) -> bool:
    return len(re.findall(r"[\u0600-\u06FF]", text)) > len(text) * 0.3


def subtitle_font(line_text: str) -> str:
    """Pick the right font based on the dominant script in a line."""
    return SUBTITLE_FONT_AR if is_arabic_text(line_text) else SUBTITLE_FONT_EN


