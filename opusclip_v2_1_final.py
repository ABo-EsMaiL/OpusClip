# B-roll blurred background
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def make_broll_frame(frame_bgr, tgt_w, tgt_h):
    src_h, src_w = frame_bgr.shape[:2]
    # Blurred + darkened background
    bg = cv2.resize(frame_bgr, (tgt_w, tgt_h), interpolation=cv2.INTER_LINEAR)
    bg = cv2.GaussianBlur(bg, (71, 71), 0)
    bg = (bg.astype(np.float32) * 0.28).clip(0, 255).astype(np.uint8)
    # Clean letterboxed panel
    fg_w = tgt_w
    fg_h = int(fg_w * src_h / src_w)
    if fg_h > int(tgt_h * 0.95):
        fg_h = int(tgt_h * 0.95)
        fg_w = int(fg_h * src_w / src_h)
        if fg_w % 2: fg_w -= 1
    fg = cv2.resize(frame_bgr, (fg_w, fg_h), interpolation=cv2.INTER_LANCZOS4)
    bd = 5
    fg = cv2.copyMakeBorder(fg, bd, bd, bd, bd,
                             cv2.BORDER_CONSTANT, value=(245, 245, 245))
    fg_h, fg_w = fg.shape[:2]
    result = bg.copy()
    y0 = max(0, (tgt_h - fg_h) // 2)
    y1 = min(tgt_h, y0 + fg_h)
    x0 = max(0, (tgt_w - fg_w) // 2)
    x1 = min(tgt_w, x0 + fg_w)
    result[y0:y1, x0:x1] = fg[:y1-y0, :x1-x0]
    return result


