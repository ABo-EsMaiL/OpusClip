# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

_detector  = dlib.get_frontal_face_detector()
_predictor = dlib.shape_predictor(DLIB_DAT)

def detect_faces(frame_bgr, frame_area, scan_scale=1.0):
    gray = cv2.cvtColor(frame_bgr, cv2.COLOR_BGR2GRAY)
    dets, scores, _ = _detector.run(gray, 1, 0.0)
    faces = []
    for rect, score in zip(dets, scores):
        fw, fh = rect.width(), rect.height()
        ar = (fw * fh) / frame_area
        if ar < 0.002: continue
        shape = _predictor(gray, rect)
        mar   = abs(shape.part(51).y - shape.part(57).y) / max(fh, 1)
        faces.append({
            "x": int((rect.left() + fw/2) / scan_scale),
            "y": int((rect.top()  + fh/2) / scan_scale),
            "w": int(fw / scan_scale), "h": int(fh / scan_scale),
            "mar_norm": round(mar, 4), "area_ratio": round(ar, 5),
        })
    return faces


