def sec2ass(sec, offset=0.0):
    t  = max(0.0, sec - offset)
    cs = int(round(t * 100))
    h  = cs // 360000
    m  = (cs % 360000) // 6000
    s  = (cs % 6000)   // 100
    c  = cs % 100
    return f"{h}:{m:02d}:{s:02d}.{c:02d}"


def build_ass(words, clip_start, clip_end, tw, th, title):
    """
    Professional karaoke subtitles — v2.2 fix:
    • Style-level Bold=-1  (libass picks heaviest bold variant correctly)
    • NO inline \\fn tags  (they caused boxes when family name not exact-matched)
    • Active word = yellow colour only, inactive = white
    • Per-line Ar/En style switch for correct font per script
    """
    fs      = max(26, int(th * 0.052))
    fs_hook = max(30, int(th * 0.058))
    outline = max(6,   int(th * 0.0045))
    shadow  = 3
    mv      = int(th * 0.07)
    mv_hook = int(th * 0.04)

    font_ar = SUBTITLE_FONT_AR   # e.g. "Tajawal"
    font_en = SUBTITLE_FONT_EN   # e.g. "Montserrat"

    header = (
        "[Script Info]\n"
        "ScriptType: v4.00+\n"
        f"PlayResX: {tw}\nPlayResY: {th}\n"
        "YCbCr Matrix: TV.601\n\n"

        "[V4+ Styles]\n"
        "Format: Name,Fontname,Fontsize,PrimaryColour,SecondaryColour,"
        "OutlineColour,BackColour,Bold,Italic,Underline,StrikeOut,"
        "ScaleX,ScaleY,Spacing,Angle,BorderStyle,Outline,Shadow,"
        "Alignment,MarginL,MarginR,MarginV,Encoding\n"

        # Bold=-1 → libass picks the heaviest bold weight from the family
        # BackColour &H99000000 = semi-transparent dark drop shadow
        f"Style: Ar,{font_ar},{fs},"
        f"&H00FFFFFF,&H000000FF,&H00000000,&H99000000,"
        f"-1,0,0,0,100,100,0,0,1,{outline},{shadow},2,20,20,{mv},1\n"

        f"Style: En,{font_en},{fs},"
        f"&H00FFFFFF,&H000000FF,&H00000000,&H99000000,"
        f"-1,0,0,0,100,100,0,0,1,{outline},{shadow},2,20,20,{mv},1\n"

        # Hook title — golden colour, top-centre
        f"Style: Hook,{font_ar},{fs_hook},"
        f"&H001ADFFF,&H000000FF,&H00000000,&HAA000000,"
        f"-1,0,0,0,100,100,0,0,1,{outline},{shadow},8,20,20,{mv_hook},1\n\n"

        "[Events]\n"
        "Format: Layer,Start,End,Style,Name,MarginL,MarginR,MarginV,Effect,Text\n"
    )

    events = ""

    # Hook title — first 3 seconds
    clean_t = clean_for_subtitle(title)
    print(f"   🔍 Title raw : {repr(title[:60])}")
    print(f"   🔍 Title clean: {repr(clean_t[:60])}")
    if clean_t:
        events += (f"Dialogue: 0,{sec2ass(clip_start,clip_start)},"
                   f"{sec2ass(clip_start+3,clip_start)},Hook,,0,0,0,,{clean_t}\n")

    # Collect words for this clip
    clip_ws = []
    for w in words:
        mid = (w["start"] + w["end"]) / 2.0
        if clip_start <= mid <= clip_end:
            txt = clean_for_subtitle(w["text"].strip())
            if txt:
                clip_ws.append({"text": txt,
                                 "start": w["start"], "end": w["end"]})
    if not clip_ws:
        return header + events

    # Group into lines: max 3 words or gap > 0.45 s
    lines, cur = [], [clip_ws[0]]
    for i in range(1, len(clip_ws)):
        gap = clip_ws[i]["start"] - clip_ws[i-1]["end"]
        if len(cur) >= 3 or gap > 0.45:
            lines.append(cur); cur = []
        cur.append(clip_ws[i])
    if cur:
        lines.append(cur)

    # Karaoke entries
    for line in lines:
        n         = len(line)
        line_text = " ".join(lw["text"] for lw in line)
        style     = "Ar" if is_arabic_text(line_text) else "En"

        for i, word in enumerate(line):
            w_start = word["start"]
            w_end   = line[i+1]["start"] if i < n-1 else line[-1]["end"]

            parts = []
            for j, lw in enumerate(line):
                t = clean_for_subtitle(lw["text"])
                if j == i:
                    # Active word: yellow — no \fn, no \b (style handles bold)
                    parts.append(f"{{\\c&H0000FFFF&}}{t}{{\\c&HFFFFFF&}}")
                else:
                    # Inactive: plain white, font from style
                    parts.append(t)

            display = " ".join(parts)
            ts = sec2ass(w_start, clip_start)
            te = sec2ass(w_end,   clip_start)
            events += f"Dialogue: 0,{ts},{te},{style},,0,0,0,,{display}\n"

    return header + events


