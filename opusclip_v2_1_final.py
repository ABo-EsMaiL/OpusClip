def gen_meta(clip):
    resp = client.chat.completions.create(
        model    = CONFIG["model"],
        messages = [
            {"role": "system", "content": META_SYS},
            {"role": "user",   "content":
                f"Title: {clip['title']}\n"
                f"Duration: {clip['duration']}s\n"
                f"Hook: {clip['hook_line']}\n"
                f"Why viral: {clip['reason']}\n"
                f"Score: {clip['virality_score']}/100"},
        ],
        temperature = 0.40,
    )
    raw = resp.choices[0].message.content.strip()
    raw = re.sub(r"^```(?:json)?\s*", "", raw, flags=re.IGNORECASE)
    raw = re.sub(r"\s*```$", "", raw)
    return json.loads(raw)

