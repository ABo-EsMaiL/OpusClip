# Word timestamp repair
# (Whisper sometimes drops first word of very short segments)
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def fill_missing_words(transcript_data: dict) -> list:
    """
    For each segment, if the word-level timestamps are missing words
    that appear in the segment text, reconstruct them by interpolating
    the available time before the first timestamped word.

    Example: segment text = "ألا وهو؟"  but words = [{"text":"وهو؟",...}]
    → reconstructs {"text":"ألا", "start": seg.start, "end": gap/1}
    """
    result = []
    for seg in transcript_data.get("segments", []):
        ts_words  = seg.get("words", [])
        txt_words = [w for w in seg["text"].strip().split() if w]

        # Enough coverage → use as-is
        if not txt_words or len(ts_words) >= len(txt_words) * 0.7:
            result.extend(ts_words)
            continue

        ts_texts = {w["text"].strip() for w in ts_words}
        missing  = [w for w in txt_words if w not in ts_texts]

        if not missing:
            result.extend(ts_words)
            continue

        # Time available before first known word
        first_known = ts_words[0]["start"] if ts_words else seg["end"]
        gap         = first_known - seg["start"]

        if gap >= 0.05 * len(missing):          # at least 50ms per word
            dt = gap / len(missing)
            # print(dt)
            # for i, mw in enumerate(missing):
            #     result.append({
            #         "text"  : mw,
            #         "start" : round(seg["start"] + i * dt, 3),
            #         "end"   : round(seg["start"] + (i + 1) * dt, 3),
            #         "prob"  : 0.60,              # estimated
            #     })

            # داخل loop الكلمات المعاد بناؤها
            for i, mw in enumerate(missing):
                clean_mw = clean_for_subtitle(mw)   # ← clean BEFORE adding
                if not clean_mw:
                    continue
                result.append({
                    "text"  : clean_mw,
                    "start" : round(seg["start"] + i * dt, 3),
                    "end"   : round(seg["start"] + (i + 1) * dt, 3),
                    "prob"  : 0.60,
                })

        result.extend(ts_words)

    return sorted(result, key=lambda w: w["start"])


