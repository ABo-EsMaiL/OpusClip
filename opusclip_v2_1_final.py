def snap_boundary(ts, segments, direction="start", window=4.0):
    best, best_d = float(ts), float("inf")
    for seg in segments:
        cand = seg["start"] if direction == "start" else seg["end"]
        d = abs(cand - float(ts))
        if d < best_d and d <= window:
            best, best_d = cand, d
    return round(best, 3)

def compress_transcript(segments, max_chars=28_000):
    """Sample evenly across the full video within char budget."""
    all_lines = [f"[{s['start']:.1f}s-{s['end']:.1f}s]: {s['text']}"
                 for s in segments]
    total = sum(len(l) for l in all_lines)
    if total <= max_chars:
        return "\n".join(all_lines)
    step = max(1, round(total / max_chars))
    out, budget = [], 0
    for i, (seg, line) in enumerate(zip(segments, all_lines)):
        if i % step != 0: continue
        if budget + len(line) + 1 > max_chars:
            out.append(f"[… {segments[-1]['end']:.0f}s total …]")
            break
        out.append(line); budget += len(line) + 1
    print(f"    Sampled {len(out)}/{len(segments)} segments "
          f"({budget:,}/{total:,} chars)")
    return "\n".join(out)

def extract_json_array(text):
    if not text or not text.strip(): return None
    cleaned = re.sub(r"^```(?:json)?\s*", "", text.strip(), flags=re.IGNORECASE)
    cleaned = re.sub(r"\s*```$", "", cleaned).strip()
    m = re.search(r'\[.*\]', cleaned, re.DOTALL)
    if m:
        try: return json.loads(m.group())
        except: pass
    try: return json.loads(cleaned)
    except: return None

