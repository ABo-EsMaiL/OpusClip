# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

class SmartDirector:
    SOLO = 0; GROUP = 1; BROLL = 2

    def __init__(self, vid_w, vid_h, src_crop_w, fps,
                 speaking_mar=0.025, min_face_area=0.003, debounce_s=0.6):
        self.vw = vid_w; self.vh = vid_h; self.cw = src_crop_w
        self.fps = fps; self.MAR_THR = speaking_mar
        self.AREA_THR = min_face_area
        self.HOLD = max(1, int(fps * debounce_s))
        self.GRP_SPREAD = src_crop_w * 0.55
        self.smooth_x = vid_w // 2
        self.state = self.BROLL; self.hold_count = 0
        self.ALPHA_SOLO = 0.07; self.ALPHA_GROUP = 0.04
        self.ALPHA_BROLL = 0.012
        self.MAX_DX = max(1, int(src_crop_w * 0.04))

    def _crop_start(self):
        return max(0, min(self.smooth_x - self.cw // 2, self.vw - self.cw))

    def update(self, faces):
        valid  = [f for f in faces if f.get("area_ratio", 0) >= self.AREA_THR]
        active = [f for f in valid  if f.get("mar_norm",   0) >  self.MAR_THR]

        if not valid:               desired = self.BROLL
        elif not active:            desired = self.state
        elif len(active) == 1:      desired = self.SOLO
        else:
            xs = [f["x"] for f in active]
            desired = self.SOLO if (max(xs)-min(xs)) < self.GRP_SPREAD else self.GROUP

        if desired != self.state:
            self.hold_count += 1
            if self.hold_count >= self.HOLD:
                self.state = desired; self.hold_count = 0
        else:
            self.hold_count = max(0, self.hold_count - 2)

        if self.state == self.SOLO:
            src = active or valid
            tx  = (sum(f["x"] for f in src) // len(src)) if src else self.vw // 2
            alpha = self.ALPHA_SOLO
        elif self.state == self.GROUP:
            src = active or valid
            xs  = [f["x"] for f in src]
            tx  = ((min(xs) + max(xs)) // 2) if xs else self.vw // 2
            alpha = self.ALPHA_GROUP
        else:
            tx = self.vw // 2; alpha = self.ALPHA_BROLL

        raw_dx = int(alpha * (tx - self.smooth_x))
        self.smooth_x += max(-self.MAX_DX, min(self.MAX_DX, raw_dx))
        return self._crop_start()


